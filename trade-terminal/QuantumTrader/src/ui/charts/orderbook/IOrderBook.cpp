#include "IOrderBook.h"
/**
 * @brief Вспомогательный статический метод для компактного форматирования чисел.
 * * Превращает длинные числа в удобный для трейдеров вид, добавляя суффиксы K и M.
 * Это экономит место на экране и не даёт цифрам в колонках слипаться.
 * * @param v Входящее число (цена, объем или итог).
 * @param decimals Количество знаков после запятой (используется только для маленьких чисел).
 * * ТАБЛИЦА РАБОТЫ ДЛЯ ПРОВЕРКИ СЕБЯ:
 * +------------------------+-------------------------+-----------------------------------------+-------------------+
 * | Если входящее v равно  | Какое условие сработает?| Что делает код внутри?                  | Что вернет метод? |
 * +------------------------+-------------------------+-----------------------------------------+-------------------+
 * | 2 531 400.0            | if (v >= 1'000'000.0)   | Делит на 1 000 000, оставляет 2 знака   | "2.53M"           |
 * |                        |                         | после запятой, приклеивает букву "M".   |                   |
 * +------------------------+-------------------------+-----------------------------------------+-------------------+
 * | 84 300.0               | if (v >= 1'000.0)       | До миллиона не дотягивает, делит на 1000| "84.30K"          |
 * |                        |                         | оставляет 2 знака, приклеивает "K".     |                   |
 * +------------------------+-------------------------+-----------------------------------------+-------------------+
 * | 12.3456                | Ни одно условие         | Число маленькое. Просто форматирует его,| "12.35"           |
 * | (при decimals = 2)     | не подошло              | округляя до указанных decimals знаков.  |                   |
 * +------------------------+-------------------------+-----------------------------------------+-------------------+
 */
static QString fmtDouble(double v, int decimals)
{
    if (v >= 1'000'000.0) return QString::number(v / 1'000'000.0, 'f', 2) + "M";
    if (v >= 1'000.0)     return QString::number(v / 1'000.0, 'f', 2) + "K";
    return QString::number(v, 'f', decimals);
}
/**
 * @brief Форматирует цену с адаптивной точностью в зависимости от дороговизны актива.
 * - >= 10000$ (BTC)     -> 1 знак после запятой (65000.1)
 * - >= 100$   (ETH,SOL) -> 2 знака после запятой (3500.50)
 * - >= 1$     (ADA,TON) -> 4 знака после запятой (2.1234)
 * - < 1$      (DOGE)    -> 6 знаков после запятой (0.071234)
 */
QString IOrderbook::formatPrice(double price)
{
    //Потом когда стакан будет готов можно поигарться с кастомизацией !! Логика Данной реализация этого приблизительная
    
    if (price >= 10000.0)return QString::number(price, 'f', 1);
    if (price >= 100.0) return QString::number(price, 'f', 2);
    if (price >= 1.0) return QString::number(price, 'f', 4);
    return QString::number(price, 'f', 6);
}
QString IOrderbook::formatQty(double qty)
{
    return fmtDouble(qty, 4);
}

QString IOrderbook::formatTotal(double total)
{
    return fmtDouble(total, 0);
}

